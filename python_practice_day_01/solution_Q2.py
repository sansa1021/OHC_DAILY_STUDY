#ㄴ방법이 두가지 소개되었다.

numbers = [3, 12, 7, 20, 5, 8, 17, 30, 9, 2]

# --- [방법 1] 정석 스타일 (for문) ---
result1 = []  # 1. 빈 바구니 준비
for num in numbers:
    # 2. 조건: 2로 나눈 나머지가 0(짝수)이고, 10보다 커야 함
    if num % 2 == 0 and num > 10:
        result1.append(num)  # 3. 담기!
print(f"정석 방법 결과: {result1}")
#ㄴfor/in으로 리스트안의 객체들에게 조건을 한번씩 적용하고 빈 result1의 리스트에 조건에 만족하는 객체들만 하나씩 담는것으로 이해함.

# --- [방법 2] 고수 스타일 (리스트 컴프리헨션) ---
# 설명: [담을놈 for 꺼낼놈 in 리스트 if 조건]
result2 = [num for num in numbers if num % 2 == 0 and num > 10]
#ㄴfor 앞뒤로 num이 오는것이 이해가 되지 않아 추가 설명요청함
#1. 구조 뜯어보기 (이름이 왜 2개일까?)
#문법은 이렇게 생겼어요: [ ①최종_변신_형태 for ②재료_이름 in ③창고 ] 
# ③ 창고 (numbers): 재료가 잔뜩 쌓여 있는 곳.
# ② 재료 이름 (num): 창고에서 하나를 꺼내서, 잠깐 부르는 별명 ("자, 이번에 꺼낸 녀석을 num이라고 부르자!").
# ① 최종 변신 형태 (num): 상자에 실제로 담을 모양.
# 2. 왜 똑같이 num이었을까?
# [ num for num in numbers ... ]
# ② 꺼낼 놈: "숫자 하나 꺼내서 num이라고 부를게."
# ① 담을 놈: "그 num을 변형 없이 그대로 상자에 넣어!"
# "꺼낸 거 그대로 넣어라" 라는 뜻이라서 똑같은 거예요!
#ㄴ라고한다. 객체를 하나 꺼내서 변형없이 넣을때는 변수명을 그대로 써도 된다고 이해함.
print(f"고수 방법 결과: {result2}")